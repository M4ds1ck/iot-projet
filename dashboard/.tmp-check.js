function simTick(){
  if(!S.simulating) return;
  Object.values(S.devices).forEach(d=>{
    if(d.status==='offline') return;
    d.ss=Math.min(100,Math.max(5,d.ss+(Math.random()-.5)*7));
    d.snr=Math.min(30,Math.max(2,d.snr+(Math.random()-.5)*3));
    if(Math.random()<.04){
      const statuses=['online','online','online','warning'];
      d.status=statuses[Math.floor(Math.random()*statuses.length)];
      updateNodeDot(d.id,d.status);
    }
    if((d.typeId==='walkie'||d.typeId==='repeater')&&!_txMap.has(d.id)&&Math.random()<.07){
      const tx=document.querySelector(`#nd-${d.id} .node-tx`);
      if(tx){tx.style.display='block';setTimeout(()=>tx.style.display='none',1400);}
    }
    // Simulate power drain for battery
    if(d.components?.power){
      d.components.power.chargePercent=Math.max(0,(d.components.power.chargePercent||100)-.01);
    }
  });
  Object.values(S.links).forEach(lk=>{
    lk.active=Math.random()<.25;
    document.getElementById('lk-'+lk.id)?.classList.toggle('active',lk.active);
  });
  syncCounts();refreshInsp();
  setTimeout(simTick,1300);
}

