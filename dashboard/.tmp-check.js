function startScan(ccId){
  const cc=S.devices[ccId];
  if(!cc){toast('Device not found','err');return;}
  if(cc.typeId!=='chemcham'){toast('Only CHEMCHAM devices can scan','err');return;}
  if(S.scanningSet.has(ccId)){toast('Already scanning…','info');return;}
  if(cc.status==='offline'){toast('Device is offline — turn it on first','err');return;}
  runScanPass(ccId);
}

async function doAutoScanLoop(){
  while(S.autoScan){
    const ccs=Object.values(S.devices).filter(d=>d.typeId==='chemcham'&&d.status!=='offline');
    if(!ccs.length){await sleep(2000);continue;}
    for(const cc of ccs){
      if(!S.autoScan) break;
      if(!S.scanningSet.has(cc.id)) await runScanPass(cc.id);
      await sleep(Math.max(400,Math.min(30000,(cc.adaptiveScanInterval||5000)/4)));
    }
  }
}

