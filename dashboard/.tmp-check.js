/* ═══ SIGNAL PHYSICS ═══ */
function calcSignal(src,dst){
  const ds=DTYPES[src.typeId]||DTYPES.walkie,dd=DTYPES[dst.typeId]||DTYPES.walkie;
  const x1=src.x+(ds.w||60)/2,y1=src.y+(ds.h||76)/2;
  const x2=dst.x+(dd.w||60)/2,y2=dst.y+(dd.h||76)/2;
  const distPx=Math.hypot(x2-x1,y2-y1);
  const distM=Math.max(1,distPx*10);
  const freqHz=(src.freq||446.006)*1e6;
  const fspl=20*Math.log10(distM)+20*Math.log10(freqHz)-147.55;
  const txDbm=10*Math.log10((src.txPower||0.5)*1000);
  const txGain=src.components?.antenna?.gainDb||2.15;
  const rxGain=dst.components?.antenna?.gainDb||2.15;
  const shadowFading=gaussianRandom(0,6);
  const fastFading=S.simulating?gaussianRandom(0,3):0;
  const rxDbm=txDbm+txGain+rxGain-fspl+shadowFading+fastFading;
  const noiseFloor=-174+10*Math.log10(12500)+8;
  const snrDb=rxDbm-noiseFloor;
  const quality=Math.round(Math.max(0,Math.min(100,(rxDbm+110)*1.1)));
  return{distM:Math.round(distM),rxDbm:+rxDbm.toFixed(1),snrDb:+snrDb.toFixed(1),noiseFloor:+noiseFloor.toFixed(1),quality,reachable:rxDbm>noiseFloor+3,mos:snrDb>30?4.5:snrDb>20?3.8:snrDb>10?2.5:snrDb>5?1.8:1.0};
}

