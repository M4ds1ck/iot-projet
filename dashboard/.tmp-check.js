function gaussianRandom(mean=0,std=1){
  const u1=Math.max(Number.EPSILON,Math.random()),u2=Math.random();
  return mean+std*Math.sqrt(-2*Math.log(u1))*Math.cos(2*Math.PI*u2);
}
function classifySignal(freq,power,bandwidth=12.5,modulation='FM'){
  const match=SIGNAL_CLASSES.find(s=>freq>=s.fMin&&freq<=s.fMax);
  if(!match) return {band:'UNKNOWN',channel:null,classification:'UNKNOWN',confidence:0,modulation:'?',description:'Unidentified signal',isKnown:false,threatLevel:'alert'};
  const widthMhz=match.chWidth?match.chWidth/1000:0;
  const channel=widthMhz?Math.max(1,Math.round((freq-match.fMin)/widthMhz)+1):null;
  const confidence=Math.abs((bandwidth||match.chWidth)-match.chWidth)<=match.chWidth*.35?.95:.72;
  let threatLevel=['PMR446','FRS','GMRS','ISM_433','LoRa_868','LoRa_915'].includes(match.name)?'safe':'monitor';
  if(power>-40) threatLevel='alert';
  return {band:match.name,channel,classification:match.name,confidence,modulation:match.modulation||modulation,description:match.description,isKnown:true,threatLevel};
}
